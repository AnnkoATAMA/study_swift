struct MorseModel {
    private let morseDict: [String: String] = [
        // カタカナ
        ".-":"イ", ".-.-":"ロ", "-...":"ハ", "-.-.":"二", "-..":"ホ",
        ".":"へ", "..-..":"ト", "..-.":"チ", "--.":"リ", "....":"ヌ",
        "..":"゛", "-.--.":"ル", ".---":"ヲ", "-.-":"ワ", ".-..":"カ",
        "--":"ヨ", "-.":"タ", "---":"レ", "---.":"ソ", ".--.":"ツ",
        "--.-":"ネ", ".-.":"ナ", "...":"ラ", "-":"ム", "..-":"ウ",
        ".-..-":"ヰ", "..--":"ノ", ".-...":"オ", "...-":"ク", ".--":"ヤ",
        "-..-":"マ", "-.--":"ケ", "--..":"フ", "----":"コ", "-.---":"エ",
        ".-.--":"テ", "--.--":"ア", "-.-.-":"サ", "-.-..":"キ", "-..--":"ユ",
        "-...-":"メ", "..-.-":"ミ", "--.-.":"シ", ".--..":"ヱ", "--..-":"ヒ",
        "-..-.":"モ", ".---.":"セ", "---.-":"ス", ".-.-.":"ン", "..--.":"゜",
        ".--.-":"ー", ".-.-.-":"、",
        
    ]


    func decode(_ morse: String) -> String {
        morse.split(separator: " ")
            .compactMap { morseDict[String($0)] }
            .joined()
    }
}
